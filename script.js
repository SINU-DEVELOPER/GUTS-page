function verifysend() {
  var val = "";
  var mobile = $("#mobile_number").val();
  var code = $("#code").val();
  if (code != undefined && code != null) {
    val = code + mobile;
  } else {
    val = mobile;
  }
  var isMobile = checkMobileRegex(val);
  var email = $("#email").val();
  var isEmail =
    /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/.test(
      email
    );

  if (!isMobile || !isEmail) {
    showAlert("Please enter valid Email Id and Mobile number", "error");
  } else {
    if (!isMobile) {
      mobile = "";
    }
    if (!isEmail) {
      email = "";
    }
    $("#verifysend").html("Sending Please Wait..");
    sendotp(mobile, email);
  }
}
function sendotp(mobile, email) {
  AjaxRequest(
    "/getData",
    {
      mobile: mobile,
      email: email,
      action: "user.sendotptoverify",
    },
    sendotpcallback
  );
}
function sendotpcallback(res) {
  if (res.data.success == true) {
    showAlert(res.data.message, "success");
    $("#verifysend").hide();
    setTimeout(function () {
      $("#verifysend").html("Resend Otp");
      $("#verifysend").show();
    }, 3000);
    $("#showOtp").show();
  } else {
    showAlert("Something Went Wrong", "error");
  }
}
